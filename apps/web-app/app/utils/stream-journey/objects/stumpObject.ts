/**
 * Procedural stump from original tree trunk pixels.
 * Fades out and self-destructs after a delay.
 */
export class StumpObject extends Container {
  private frameCount = 0
  private readonly LIFETIME = 600 // ~10 sec at 60fps

  constructor(variant: number, scale: number) {
    super()

    const g = new Graphics()
    const index = variant % STUMPS.length
    const data = STUMPS[index]!
    const anchor = ANCHORS[index]!

    for (const [x, y, color] of data) {
      g.rect(x, y, 1, 1).fill(color)
    }

    g.x = -anchor[0]!
    g.y = -anchor[1]! - 1

    this.addChild(g)
    this.scale.set(scale)
  }

  update() {
    this.frameCount++

    const fadeStart = this.LIFETIME - 180
    if (this.frameCount > fadeStart) {
      this.alpha = Math.max(0, 1 - (this.frameCount - fadeStart) / 180)
    }

    return this.frameCount < this.LIFETIME
  }
}
